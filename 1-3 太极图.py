import turtle as t
t.screensize(350, 350, "yellow") #不设背景(默认白色)也可以，先画黑框的整个大白圆，然后只绘制黑鱼
t.setup(400,400)#绘制黑色的部分(阴)
t.speed(100)
t.penup()
t.goto(0,150)
t.pendown()
t.color("black","black")
t.begin_fill()
t.circle(-150,180)
t.left(180)
t.circle(75,180)
t.circle(-75,180)
t.end_fill()#绘制白色的部分(阳)
t.left(180)
t.color("white","white")
t.begin_fill()
t.circle(150,180)
t.circle(75,180)
t.circle(-75,180)
t.end_fill()
#绘制黑色部分中的白色鱼眼(阴中阳)
t.penup()
t.goto(0,50)
t.pendown()
t.color("white","white")
t.begin_fill()
t.circle(25)
t.end_fill()#绘制白色部分中的黑色鱼眼(阳中阴)
t.penup()
t.goto(0,-50)
t.pendown()
t.color("black","black")
t.begin_fill()
t.circle(-25)
t.end_fill()
t.penup()
t.goto(50,-170)
t.pendown()
t.write("xxxxxx",font=("Arial",20,"normal"))
t.hideturtle()
t.done()