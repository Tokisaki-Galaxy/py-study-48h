
import turtle as t
t.setup(800,500)
t.shape("turtle")
t.pensize(20)
t.speed(100)        #依次绘制五环
t.penup()
t.goto(-240,200)
t.pendown()
t.color("blue")
t.circle(-100)
t.penup()
t.goto(0,200)
t.pendown()
t.color("black")
t.circle(-100)
t.penup()
t.goto(240,200)
t.pendown()
t.color("red")
t.circle(-100)
t.penup()
t.goto(-120,100)
t.pendown()
t.color("yellow")
t.circle(-100)
t.penup()
t.goto(120,100)
t.pendown()
t.color("green")
t.circle(-100)


 #补绘嵌套效果
t.penup()
t.goto(-240,200)
t.setheading(0)
t.pendown()
t.color("blue")
t.circle(-100,120)
t.penup()
t.goto(0,0)
t.setheading(180)
t.pendown()
t.color("black")
t.circle(-100,30)
t.penup()
t.goto(0,200)
t.setheading(0)
t.pendown()
t.circle(-100,120)
t.penup()
t.goto(240,0)
t.setheading(180)
t.pendown()
t.color("red")
t.circle(-100,30)

t.penup()
t.goto(50,-170)
t.pendown()
t.write("xxxxxx",font=("Arial",20,"normal"))
t.hideturtle()
t.done()